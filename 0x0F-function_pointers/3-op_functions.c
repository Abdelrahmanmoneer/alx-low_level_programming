#include "3-calc.h"
/**
  * op_add - the sum of two int
  * @a: int 1
  * @b: int 2
  *
  * Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - the sub of two int
  * @a: int 1
  * @b: int 2
  *
  * Return: sub
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - the mult of two int
  * @a: int 1
  * @b: int 2
  *
  * Return: mult
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the division of two int
 * @a: int 1
 * @b: int 2
 *
 * Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - the modulo of two int
  * @a:int 1
  * @b: int 2
  *
  * Return: mod
*/

int op_mod(int a, int b)
{
	return (a % b);
}








